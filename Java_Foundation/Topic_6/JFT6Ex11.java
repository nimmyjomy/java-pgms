public class JFT6Ex11{

 public static void main(String[] args){
   int noOfFishInTank = 3;
   switch(noOfFishInTank){
    case 0:
	  System.out.println("There is no fish in the tank");
	  break;
	case 1:
	  System.out.println("There is one fish in the tank");
	  break;
	case 2:
	  System.out.println("There is two fish in the tank");
	  break;
	case 3:
	  System.out.println("There is three fishes in the tank");
	  break;
	default:
	  System.out.println("An indeterminate number of fish in the tank");
	 }
}
}	 
	  